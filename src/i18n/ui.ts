export type Lang = 'fr' | 'en';

/** Segments de routes par langue (utilisés par la nav et le sélecteur de langue). */
export const routes = {
  home: { fr: '', en: '' },
  news: { fr: 'actualites', en: 'news' },
  courses: { fr: 'cours', en: 'courses' },
  resources: { fr: 'ressources', en: 'resources' },
  papers: { fr: 'sujets', en: 'papers' },
  generator: { fr: 'generateur', en: 'generator' },
  about: { fr: 'a-propos', en: 'about' },
  contact: { fr: 'contact', en: 'contact' },
  legal: { fr: 'mentions-legales', en: 'legal' },
} as const;

export type RouteKey = keyof typeof routes;

export function localePath(lang: Lang, key: RouteKey, suffix = ''): string {
  const seg = routes[key][lang];
  return `${lang}/${seg ? seg + '/' : ''}${suffix}`;
}

